Khi cha Phêrô Nguyễn Phước Chính được bổ nhiệm làm Cha sở đầu tiên vào năm 1898, họ đạo Hạnh Thông Tây chỉ có một miếng đất, chung quanh là nơi cho các bổn đạo ở. Cha Phêrô Chính đã xin phép các quan chức địa phương cho xây cất nhà thờ mới trên mảnh đất này đồng thời xây cất nhà xứ và trường học xung quanh.[5] Năm 1921, thời linh mục Matthêu Hồ Tấn Đức làm Cha sở, nhà thờ Hạnh Thông Tây mới được xây dựng trên ba thửa đất rộng 2,1 mẫu của ông Giuse Hồ Văn Chua hiến cho nhà thờ trước đó. Toàn bộ chi phí xây cất đều được vợ chồng ông Denis Lê Phát An tự nguyện đóng góp.

