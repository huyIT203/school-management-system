                        Họ đạo Hạnh Thông Tây được giám mục Puginier thành lập năm 1861. Thời đầu, có gia đình ông Đốc phủ Ca và một vài người giàu có trong làng Hạnh Thông Tây xin gia nhập đạo. Dần dần, khoảng 400 người ngoại giáo có của cải và nhà cửa trong làng đến xin học đạo. Vì nơi đây là khu vực ngoại thành, dân cư chủ yếu là người nghèo nên họ đạo chưa có nơi tụ họp, giảng dạy. Sau này, một số người khá giả trong làng đã hiến tặng ngôi đình của họ mà dựng lên ngôi nhà nguyện đầu tiên của họ đạo Hạnh Thông Tây.

